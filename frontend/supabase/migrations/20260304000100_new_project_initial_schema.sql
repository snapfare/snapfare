-- ─── Enable extensions ────────────────────────────────────────────────────────
CREATE EXTENSION IF NOT EXISTS "uuid-ossp";
CREATE EXTENSION IF NOT EXISTS "pgcrypto";

-- ─── deals ───────────────────────────────────────────────────────────────────
-- Full final schema after all migrations (no legacy columns)
CREATE TABLE IF NOT EXISTS public.deals (
  id bigint GENERATED BY DEFAULT AS IDENTITY NOT NULL,
  title text NULL,
  price double precision NULL,
  currency text NULL,
  link text NULL,
  booking_url text NULL,
  skyscanner_url text NULL,
  source text NULL,
  origin text NULL,
  destination text NULL,
  origin_iata text NULL,
  destination_iata text NULL,
  airline text NULL,
  aircraft text NULL,
  cabin_class text NULL,
  stops integer NULL,
  date_out date NULL,
  date_in date NULL,
  miles text NULL,
  flight_duration_minutes integer NULL,
  flight_duration_display text NULL,
  baggage_included boolean NULL,
  baggage_pieces_included smallint NULL,
  baggage_allowance_kg integer NULL,
  image text NULL,
  tier text NULL DEFAULT 'free',
  travel_period_display text NULL,
  scoring text NULL,
  expires_in text NULL,
  created_at timestamp with time zone DEFAULT now(),
  CONSTRAINT deals_pkey PRIMARY KEY (id),
  CONSTRAINT deals_booking_url_key UNIQUE (booking_url)
) TABLESPACE pg_default;

-- Indexes for common query patterns
CREATE INDEX IF NOT EXISTS deals_source_idx ON public.deals (source);
CREATE INDEX IF NOT EXISTS deals_tier_idx ON public.deals (tier);
CREATE INDEX IF NOT EXISTS deals_origin_iata_idx ON public.deals (origin_iata);
CREATE INDEX IF NOT EXISTS deals_destination_iata_idx ON public.deals (destination_iata);
CREATE INDEX IF NOT EXISTS deals_cabin_class_idx ON public.deals (cabin_class);
CREATE INDEX IF NOT EXISTS deals_stops_idx ON public.deals (stops);
CREATE INDEX IF NOT EXISTS deals_source_date_out_idx ON public.deals (source, date_out);
CREATE INDEX IF NOT EXISTS deals_scoring_idx ON public.deals (scoring);

-- RLS: anon can read deals (frontend display)
ALTER TABLE public.deals ENABLE ROW LEVEL SECURITY;
CREATE POLICY "anon can read deals" ON public.deals FOR SELECT TO anon USING (true);
CREATE POLICY "auth can read deals" ON public.deals FOR SELECT TO authenticated USING (true);
-- Backend writes via service_role key (bypasses RLS) — no client insert policy needed

-- ─── source_articles ─────────────────────────────────────────────────────────
CREATE TABLE IF NOT EXISTS public.source_articles (
  id bigint GENERATED BY DEFAULT AS IDENTITY NOT NULL,
  created_at timestamp with time zone NOT NULL DEFAULT now(),
  article_url text NOT NULL,
  first_seen_at timestamp with time zone NULL DEFAULT now(),
  last_scraped_at timestamp with time zone NULL,
  status text NULL DEFAULT 'pending',
  source text NULL,
  last_error text NULL,
  CONSTRAINT source_articles_pkey PRIMARY KEY (id),
  CONSTRAINT source_articles_article_url_key UNIQUE (article_url)
